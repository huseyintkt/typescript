enum Direction {
    top, // 0
    right, // 1
    bottom, // 2
    left, // 3
}

console.log(Direction.left); // 3

enum Color {
    red = 'red',
    blue = 'blue',
    yellow = 'yellow',
}

console.log(Color.red); // red

enum Days {
    monday = 1,
    tuesday = 2,
    wednesday = 3,
    thursday = 4,
    friday = 5,
    saturday = 6,
    sunday = 7,
}

console.log(Days.friday); // 5
